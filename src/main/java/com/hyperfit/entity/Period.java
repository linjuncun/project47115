package com.hyperfit.entity;

/**
 * <p> 课程周期实体 </p>
 */
public class Period {
    private Integer weekDay;
    private String time;

    public Integer getWeekDay() {
        return weekDay;
    }

    public void setWeekDay(Integer weekDay) {
        this.weekDay = weekDay;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
